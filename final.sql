CREATE TRIGGER delete_s_id
AFTER DELETE ON advisor
FOR EACH ROW
BEGIN
  DELETE FROM student WHERE s_id = OLD.s_id;
END;



CREATE TRIGGER insert_advisor_id
AFTER INSERT ON advisor
FOR EACH ROW
BEGIN
  INSERT INTO advisor (advisor_id, salary)
  VALUES ('90140', (SELECT AVG(salary) FROM advisor));
END;


CREATE TRIGGER update_balance
AFTER UPDATE OF gpa ON accouting
FOR EACH ROW
BEGIN
    UPDATE accouting SET balance = old.balance + 1000 where gpa <> old.gpa;
END;

